"""工具函数模块 - 包含激活函数和损失函数"""

import numpy as np


def sigmoid(z):
    """
    Sigmoid 激活函数
    
    数学公式: σ(z) = 1 / (1 + e^(-z))
    
    参数:
        z: 加权输入，可以是标量、向量或矩阵
    
    返回:
        激活值，范围在 (0, 1) 之间
    
    功能说明:
        - 将任意实数映射到 (0, 1) 区间
        - 常用于二分类问题的输出层
        - 优点：平滑可导，输出概率化
        - 缺点：易出现梯度消失（当 z 绝对值很大时）
    """
    return 1 / (1 + np.exp(-z))


def sigmoid_prime(a):
    """
    Sigmoid 函数的导数
    
    数学公式: σ'(a) = a * (1 - a)，其中 a = σ(z)
    
    参数:
        a: Sigmoid 的输出值（而非原始输入 z）
    
    返回:
        Sigmoid 函数在该点的导数值
    
    功能说明:
        - 在反向传播中用于计算梯度
        - 接收激活值 a 而非原始输入 z，避免重复计算 sigmoid
        - 导数范围: (0, 0.25]，在 a=0.5 时取得最大值 0.25
    """
    return a * (1 - a)


def mse_loss(y_true, y_pred):
    """
    均方误差损失函数
    
    数学公式: J = (1/(2m)) * Σ(y_pred - y_true)²
    
    参数:
        y_true: 真实标签，shape=(m, output_size)
        y_pred: 预测值，shape=(m, output_size)
    
    返回:
        标量损失值
    
    功能说明:
        - 衡量预测值与真实值之间的平均平方差
        - 除以 2 是为了在求导时抵消系数，简化计算
        - 常用于回归问题或二分类的输出层
    
    注意事项:
        1. 量纲问题：MSE 结果的单位是原数据单位的平方
           如果想保持原单位，可以使用 RMSE（对 MSE 开根号）
        
        2. 对异常值敏感：平方运算会放大离群点的影响
           例如误差是 10，平方后是 100；误差是 100，平方后是 10000
        
        3. 输入形状：要求 y_true 和 y_pred 形状完全一致
        
        4. 数值稳定性：如果预测值和真实值非常大，可能导致数值溢出
           实际使用时通常先对数据进行归一化处理
    """
    m = y_true.shape[0]  # 样本数量
    return np.sum((y_pred - y_true) ** 2) / (2 * m)